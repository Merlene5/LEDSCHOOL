        // Fonction pour détecter le défilement
        const revealSections = () => {
            const headings = document.querySelectorAll("#content h2");
            const paragraphs = document.querySelectorAll("#content p");
            const windowHeight = window.innerHeight;

            headings.forEach((h, index) => {
                const rectH = h.getBoundingClientRect();
                const rectP = paragraphs[index].getBoundingClientRect();

                // Vérifie si le titre et le paragraphe sont visibles dans la fenêtre
                if (rectH.top < windowHeight && rectH.bottom > 0) {
                    setTimeout(() => {
                        h.classList.add("visible");
                        paragraphs[index].classList.add("visible");
                    }, index * 600); // Délai basé sur l'index
                }
            });
        };

        // Écoute l'événement de défilement
        window.addEventListener('scroll', revealSections);
        // Appelle la fonction au chargement de la page pour vérifier si des éléments sont déjà visibles
        window.addEventListener('load', revealSections);
